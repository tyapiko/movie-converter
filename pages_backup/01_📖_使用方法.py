import streamlit as st

st.set_page_config(
    page_title="使用方法",
    page_icon="📖",
    layout="wide"
)

st.title("📖 使用方法")

# タブで各ツールの使用方法を整理
tab1, tab2, tab3 = st.tabs(["ショート動画変換", "動画結合", "スライド動画作成"])

with tab1:
    st.header("🎬 ショート動画変換")
    st.markdown("""
    ### 使用手順
    1. **動画をアップロード**: MP4、AVI、MOV、MKV形式の動画ファイルを選択
    2. **テキスト追加（オプション）**: テロップを追加したい場合はチェック
    3. **変換実行**: 「ショート動画に変換」ボタンをクリック
    4. **ダウンロード**: 変換完了後、ダウンロードボタンでファイルを保存
    
    ### YouTubeショート仕様
    - **解像度**: 1080x1920 (9:16)
    - **最大時間**: 60秒
    - **フォーマット**: MP4 (H.264)
    
    ### 機能詳細
    - **リサイズ**: 元動画を9:16の縦型に自動変換
    - **テロップ機能**: 時間指定でテキストを追加可能
    - **音声合成**: VOICEVOX連携で日本語音声を生成
    - **BGM追加**: 背景音楽の追加と音量調整
    """)

with tab2:
    st.header("🔗 動画結合")
    st.markdown("""
    ### 使用手順
    1. **動画を複数選択**: MP4、AVI、MOV、MKV形式の動画ファイルを2つ以上選択
    2. **結合実行**: 「動画を結合」ボタンをクリック
    3. **ダウンロード**: 結合完了後、ダウンロードボタンでファイルを保存
    
    ### 注意事項
    - **順番**: 動画は選択した順番で結合されます
    - **解像度**: 異なる解像度の動画も結合可能です
    - **フォーマット**: 最終出力はMP4形式になります
    
    ### 結合仕様
    - 各動画のフレームレートは自動調整されます
    - 音声トラックも含めて結合されます
    - ファイルサイズが大きい場合は処理時間がかかります
    """)

with tab3:
    st.header("📊 スライド動画作成")
    st.markdown("""
    ### 使用手順
    1. **PowerPointアップロード**: .pptx/.ppt ファイルを選択
    2. **BGMアップロード**: MP3、WAV等の音楽ファイルを選択
    3. **表示時間設定**: 各スライドの表示時間を個別に設定
    4. **歌詞追加**: 歌詞テキストと表示タイミングを設定（オプション）
    5. **動画生成**: 「スライド動画を生成」ボタンをクリック
    6. **ダウンロード**: 生成完了後、ダウンロードボタンでファイルを保存
    
    ### 出力仕様
    - **解像度**: 1920x1080 (16:9)
    - **フォーマット**: MP4 (H.264)
    - **歌詞機能**: 時間同期した歌詞表示
    
    ### 機能詳細
    - **スライド変換**: PowerPointを画像に変換して動画化
    - **BGM同期**: 背景音楽の長さに合わせて動画時間を調整
    - **歌詞表示**: カラオケ風の歌詞表示機能
    - **日本語フォント**: 日本語テキストの美しい表示
    """)

st.markdown("---")
st.markdown("💡 **ヒント**: 各ツールは独立しており、用途に応じて使い分けができます。")